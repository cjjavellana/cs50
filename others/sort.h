
/**
 * Sorts an array of integer (referred to by *iArray) 
 * using bubble sort method. 
 * 
 * This function modifies the contents original array.
 */
void bubbleSort(int *iArray, int arraySize);

/**
 * Sorts an array of integer (referred to by *iArray)
 * using the selection sort method.
 *
 * This function modifies the contents of the original array.
 */
void selectionSort(int *iArray, int arraySize);

/**
 * Sorts the given array (*iArray) using insertion sort method
 *
 * This function modifies the contents of the origin array
 */
void insertionSort(int *iArray, int);
void mergeSort(int *iArray, int);
void quickSort(int *iArray, int);
